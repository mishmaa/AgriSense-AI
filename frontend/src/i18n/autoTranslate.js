const dictionaries = { zh, ur };
const reverse = Object.fromEntries(
  Object.entries(dictionaries).map(([language, dictionary]) => [
    language,
    Object.fromEntries(Object.entries(dictionary).map(([english, localized]) => [localized, english])),
  ]),
);

let observer;
let currentLanguage = 'en';

function sortedEntries(dictionary) {
  return Object.entries(dictionary).sort((a, b) => b[0].length - a[0].length);
}

function replaceText(text, dictionary) {
  const exact = dictionary[text.trim()];
  if (exact) {
    return `${text.match(/^\s*/)?.[0] || ''}${exact}${text.match(/\s*$/)?.[0] || ''}`;
  }

  let next = text;
  for (const [source, target] of sortedEntries(dictionary)) {
    if (next.includes(source)) {
      next = next.split(source).join(target);
    }
  }
  return next;
}

function normalizeFromPreviousLanguage(text, language) {
  if (language === 'zh') {
    return replaceText(text, reverse.ur);
  }
  if (language === 'ur') {
    return replaceText(text, reverse.zh);
  }
  return replaceText(replaceText(text, reverse.zh), reverse.ur);
}

function translateValue(value, language) {
  const normalized = normalizeFromPreviousLanguage(value, language);
  if (language === 'en') return normalized;
  return replaceText(normalized, dictionaries[language] || {});
}

function shouldSkipElement(element) {
  return ['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE', 'PRE'].includes(element?.tagName);
}

function translateTextNode(node, language) {
  const parent = node.parentElement;
  if (!node.nodeValue?.trim() || shouldSkipElement(parent)) return;
  const translated = translateValue(node.nodeValue, language);
  if (translated !== node.nodeValue) {
    node.nodeValue = translated;
  }
}

function translateAttributes(element, language) {
  if (shouldSkipElement(element)) return;
  for (const attribute of ['placeholder', 'aria-label', 'title', 'alt']) {
    const value = element.getAttribute?.(attribute);
    if (value) {
      const translated = translateValue(value, language);
      if (translated !== value) element.setAttribute(attribute, translated);
    }
  }
  if (element instanceof HTMLInputElement && element.value && document.activeElement !== element) {
    const translated = translateValue(element.value, language);
    if (translated !== element.value) element.value = translated;
  }
}

function walk(root, language) {
  if (!root) return;
  if (root.nodeType === Node.TEXT_NODE) {
    translateTextNode(root, language);
    return;
  }
  if (root.nodeType !== Node.ELEMENT_NODE && root.nodeType !== Node.DOCUMENT_NODE) return;

  if (root.nodeType === Node.ELEMENT_NODE) {
    translateAttributes(root, language);
  }

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT);
  let node = walker.nextNode();
  while (node) {
    if (node.nodeType === Node.TEXT_NODE) {
      translateTextNode(node, language);
    } else {
      translateAttributes(node, language);
    }
    node = walker.nextNode();
  }
}

export function installAutoTranslator(language) {
  currentLanguage = language;
  observer?.disconnect();

  const apply = () => walk(document.body, currentLanguage);
  requestAnimationFrame(apply);
  window.setTimeout(apply, 80);
  window.setTimeout(apply, 320);

  observer = new MutationObserver((mutations) => {
    observer.disconnect();
    for (const mutation of mutations) {
      if (mutation.type === 'characterData') {
        translateTextNode(mutation.target, currentLanguage);
      }
      for (const node of mutation.addedNodes) {
        walk(node, currentLanguage);
      }
      if (mutation.type === 'attributes') {
        translateAttributes(mutation.target, currentLanguage);
      }
    }
    observer.observe(document.body, {
      subtree: true,
      childList: true,
      characterData: true,
      attributes: true,
      attributeFilter: ['placeholder', 'aria-label', 'title', 'alt', 'value'],
    });
  });

  observer.observe(document.body, {
    subtree: true,
    childList: true,
    characterData: true,
    attributes: true,
    attributeFilter: ['placeholder', 'aria-label', 'title', 'alt', 'value'],
  });

  return () => observer?.disconnect();
}
